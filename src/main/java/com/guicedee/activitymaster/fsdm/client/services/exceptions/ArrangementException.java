package com.guicedee.activitymaster.fsdm.client.services.exceptions;

public class ArrangementException
		extends ActivityMasterException
{
	public ArrangementException()
	{
	}

	public ArrangementException(String message)
	{
		super(message);
	}

	public ArrangementException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public ArrangementException(Throwable cause)
	{
		super(cause);
	}

	public ArrangementException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace)
	{
		super(message, cause, enableSuppression, writableStackTrace);
	}
}
